package com.bl.basicprogram;

public class Swap
{
    public static void main(String[] args) {
        int a=4;
        int b=5;
        System.out.println("Befor swaping : "+a +" " +b);
        int swap1=b;
        int swap2=a;
        System.out.println("after swapping : "+swap1 +" " +swap2);

    }
}
